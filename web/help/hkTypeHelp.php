
<h2>Housekeeping Type</h2>
<p>The AWARE database contains three types of housekeeping data.</p>
<ul>
<li>Sensor Hk -- This includes all the environmental data on the status of the station, such as temperatures, voltages and currents.</li>
<li>Event Hk -- This includes all the data related to the performance of the trigger, such as thresholds and L1,L2,L3, etc. scaler rates counting the number of triggers at each point.</li>
<li>Header -- This data is derived from the actually events and so includes things such as event rate and channel RMS.</li>
</ul>