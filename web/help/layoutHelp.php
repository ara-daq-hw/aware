
<h2>Layout</h2>
<p>The AWARE event display has a number of predefined waveform views:</p>
<ul>
<li>10x2 -- A 10x2 array of all 20 antennas per station</li>
<li>5x4 -- A 5x4 array of all 20 antennas per station, this is currently the default</li>
<li>VPol Only -- A 4x2 array of the 8 vertically polarised antennas</li>
<li>HPol Only -- A 4x2 array of the 8 horizontally polarised antennas</li>
</ul>